import sys

file_path = r'd:\Users\lorns\OneDrive\第二职业\网文写作\坍缩侦探\小说大纲\小说总大纲.md'

with open(file_path, 'r', encoding='utf-8') as f:
    content = f.read()

old_start = '## 作者创作建议'
old_end = '## References'
idx_start = content.find(old_start)
idx_end = content.find(old_end)

if idx_start == -1 or idx_end == -1:
    print(f"ERROR: Could not find markers. start={idx_start}, end={idx_end}")
    sys.exit(1)

new_section = (
    "## 作者创作建议\n\n"
    "### 1. 风格把控\n\n"
    "* \u201c观测即定型\u201d不是写科幻论文，而是写\u201c摆在桌上的东西和上一秒不一样\u201d。"
    "量子机制每次出场，必须通过物件、字段、屏幕截图或人物动作来告诉读者，而不是用旁白解释。"
    "凡是\u201c读者用眼睛看不见\u201d的概念，就不该写进正文。\n\n"
    "* 证据链写成动作序列：拿到\u2192对照\u2192发现差异\u2192回查\u2192碰壁或碰线索，"
    "不允许写成\u201c分析之后得出结论\u201d的议论文口吐。\n\n"
    "* 恐惧优先写\u201c系统失灵\u201d而不是\u201c恶人倒地\u201d："
    "门禁不认你、监控只给你灰屏、回执倒签、统一话术复读\u2014\u2014"
    "这类\u201c熟悉体系失灵\u201d比血腥场景更容易让读者产生代入恐惧。\n\n"
    "### 2. 节奏控制\n\n"
    "* **单元案节奏**：每卷 5 个单元案，每案必须供血主线拼图；"
    "每 10 章左右一个小高潮（证据推进/风险升级/代价落地），"
    "每 30 章左右一个结构性翻转。\n\n"
    "* **能力与反噬同步**：观测次数越多，反噬越重\u2014\u2014"
    "记忆断层变长、身份认知漂移加剧、\u201c案卷版的我\u201d越来越实；"
    "不要用具体数值（\u201c增加 1 秒\u201d\u201c提高 20%\u201d）写反噬，"
    "而是写成\u201c上一次只是头痛，这一次在镜子里看见了不该在的影子\u201d这种可感变化。\n\n"
    "* **悬念节拍**：每卷结尾必须留一个\u201c可追问点\u201d"
    "（人/物/地点/规则/动机五选一），每两卷完成一次结构性翻案。\n\n"
    "### 3. 人物塑造\n\n"
    "* **陆深**：不是\u201c理性与情感的冲突\u201d这种抽象标签，"
    "而是一个\u201c明知触碰证物违规，但实在忍不住\u201d的人。"
    "他的快判断冲动跟证据链信仰是内在矛盾，不需要额外解释。"
    "写化时优先写他的手、眼神和生理反应，而不是内心独白。\n\n"
    "* **操盘者/执行者**：反派不需要是镜像怪物。"
    "真正的威胁是\u201c能动材料与权限的人\u201d与"
    "\u201c愿意牺牲他人换体面结案的机制\u201d。"
    "手段必须落到可复核动作：投喂材料、剪切笔录、降级算法、偷换封存、逼人签字。\n\n"
    "* **配角群像**：每个关键配角都有自己的执念和秘密"
    "（见上文\u201c人物缺陷-秘密-执念三角\u201d表）。写配角时先查三角卡，不要自己另起炉灶。\n\n"
    "### 4. 核心主题\n\n"
    "* **身份认同的回答必须通过动作给出**："
    "\u201c我是谁\u201d的答案不是旁白\u201c自我是选择的结果\u201d，"
    "而是陆深在证据与快判断之间反复选择\u201c先落纸\u201d的动作序列。\n\n"
    "* **观测者悖论的落地方法**：不写\u201c真相是否存在\u201d的哲学议论，"
    "而是写\u201c我知道这份导出被改过，但如果我不观测原始日志，"
    "改过的版本就会变成唯一版本\u201d这种\u201c不看就会被写死\u201d的具象恐惧。\n\n"
    "* **技术伦理不写成结论，写成代价**：织女星计划的\u201c意识永生\u201d初衷"
    "只需要在日志/档案里出现一行，让读者看见\u201c好意图的后果\u201d就够了。\n\n"
    "### 5. 技术与戏剧的分界（冲高治理原则）\n\n"
    "* 技术只负责压回差异，不负责替代戏剧。"
    "量子机制/版本链/哈希/编号矩阵提供的是\u201c把差异压到桌面上、逼对手回应\u201d的工具。"
    "高潮永远是人物在压差面前做出的选择与付出的代价，不允许让技术解释本身变成高潮。\n\n"
)

content = content[:idx_start] + new_section + content[idx_end:]

with open(file_path, 'w', encoding='utf-8') as f:
    f.write(content)

print(f'Done. Start={idx_start}, End={idx_end}, New length={len(content)}')
