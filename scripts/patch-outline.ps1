$state = "normal"
$tableRowCount = 0

for ($i = 0; $i -lt $lines.Length; $i++) {
    $line = $lines[$i]

    # -- Change A: 替换 3.5.1 标题和说明行 --
    if ($line -match "### 3\.5\.1 势力关系与责任漂移图（势力模板吸收）") {
        # 写入新标题和说明（跳过原有的 2 行：标题 + 空行 + 说明行）
        foreach ($nl in $new351Header) { $newLines.Add($nl) }
        $i++ # skip 空行
        $i++ # skip > 用途：...行
        $state = "in351table"
        $tableRowCount = 0
        continue
    }

    # 在 3.5.1 表格之后、--- 分隔线之前插入模板四类分组
    if ($state -eq "in351table") {
        if ($line -match "^\| ") {
            $newLines.Add($line)
            continue
        } elseif ($line.Trim() -eq "---") {
            # 插入模板分组内容，然后写入 ---
            foreach ($nl in $new351Body) { $newLines.Add($nl) }
            $state = "normal"
            # 不写入原有的 ---，因为 new351Body 已经包含了 ---
            $i++ # skip the empty line after ---
            continue
        } else {
            $newLines.Add($line)
            continue
        }
    }

    # -- Change C: 在交织执行口令行之后插入明暗线起承转合 --
    if ($line -match "\*\*交织执行口令\*\*") {
        $newLines.Add($line)
        foreach ($nl in $newMingAnXian) { $newLines.Add($nl) }
        continue
    }

    # -- Change D: 在 C01 证据链行之后插入参与人物 --
    if ($line -match "- 证据链：会议邀约与参会人员") {
        $newLines.Add($line)
        foreach ($nl in $newC01Participants) { $newLines.Add($nl) }
        continue
    }

    $newLines.Add($line)
}

